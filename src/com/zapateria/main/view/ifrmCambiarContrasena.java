    private void btnCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarActionPerformed
        // TODO add your handling code here:

        String contrasena = String.copyValueOf(passContrasena.getPassword());
        String confirmarContrasena = String.copyValueOf(passConfirmarContrasena.getPassword());

        if (contrasena.length() > 2 && confirmarContrasena.length() > 2) {
            if (contrasena.equals(confirmarContrasena)) {
                if (servicio.cambiarContrasena(idUsuario, contrasena)) {
                    mostrarMensaje("Se ha cambiado la contraseña satisfactoriamente");
                    this.dispose();
                } else {
                    mostrarMensaje("No se pudo cambiar la contraseña");
                }
            } else {
                mostrarMensaje("Las constraseñas no coinciden");
            }
        } else {
            mostrarMensaje("Escriba una contraseña mas larga");
        }
    }//GEN-LAST:event_btnCambiarActionPerformed

